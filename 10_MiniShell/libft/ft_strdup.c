#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	int		i;

	i = 0;
	dst = ((char *)malloc(sizeof(char) * (ft_strlen(s) + 1)));
	if (dst == NULL)
		return (0);
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

